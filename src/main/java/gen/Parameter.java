package gen;

public class Parameter {
	private int id;
	private String svc_name;
	private String svc_url;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getSvc_name() {
		return svc_name;
	}

	public void setSvc_name(String svc_name) {
		this.svc_name = svc_name;
	}

	public String getSvc_url() {
		return svc_url;
	}

	public void setSvc_url(String svc_url) {
		this.svc_url = svc_url;
	}

}
